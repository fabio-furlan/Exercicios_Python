# operador        operação

# 1  =               recebe
# 2  ==              igual
# 3  !=              diferente
# 4  >               maior
# 5  <               menor 
# 6  >=              maior ou igual
# 7  <=              menor ou igual      


# variavel
x = 2
y = 3

# exemplo 1 recebe
# x = y
print (x)

#exemplo 2 igual 
print (x == y)

#exemplo 3 diferente
print (x != y)

#exemplo 4 maior 
print(x > y)

#exemplo 5 menor 
print(x < y)

#exemplo 6 maior ou igual
print(x >= y)

#exemplo 7 menor ou igual
print(x <= y)

